x = float(input("Введите число: "))
b = 0

if x <= 0:
    print("Число должно быть больше нуля.")
elif x < 1:
    while x < 1:
        x *= 10
        b -= 1
    print(f"Формат плавающей точки: x = {x} * 10 ** {b}")
else:
    while x >= 10:
        x /= 10
        b += 1
    print(f"Формат плавающей точки: x = {x} * 10 ** {b}")
